package ch01;

public class HelloJava {
	
	// 코드의 시작점(메인함수)
	public static void main(String[] args) {
		
		// 주석 (컴파일러가 무시하는 코드)
		// ->화면에 글자를 출력하는 명령어.
		System.out.println("Hello JAVA "); // 출력값 : Hello JAVA
		// 저장 --> JAVAC(컴파일) HelloJava.java
		// HelloJava.class
		
		// java HelloJava.java (실행)
		
		// 컴파일 시점 <- class
		// 런타임 (시점) <- 프로그램 실행

	} // end of main
	// 중괄호에 유효범위는 절대적이다.

} //end of class
