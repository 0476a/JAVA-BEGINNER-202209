		System.out.println(num + "  * 8 = " + (num * 8)); // 출력값 : 2 * 8 = 16
		System.out.println(num + "  * 9 = " + (num * 9)); // 출력값 : 2 * 9 = 18
		// 출력값 : (2 * 1 = 2) ~ (2 * 9 = 18)
		System.out.println("-------------------------");

